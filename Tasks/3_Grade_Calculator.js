let marks = 101;
if(100 < marks){
    console.log("Result Error !")
}else if(90 <= marks){
    console.log("A+")
}else if(80 <= marks){
    console.log("B")
}else if(70 <= marks){
    console.log("C")
}else if(60 <= marks){
    console.log("D")
}else if(0 <= marks){
    console.log("F")
}else{
    console.log("Result Error !")
}